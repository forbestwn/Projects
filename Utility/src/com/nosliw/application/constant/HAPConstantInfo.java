package com.nosliw.application.constant;

public class HAPConstantInfo{
	public String name;
	public String value;
	public String type;
	public String skip;
	
	public HAPConstantInfo(String name, String value, String type, String skip){
		this.name = name;
		this.value = value;
		this.type = type;
		this.skip = skip;
	}
}
